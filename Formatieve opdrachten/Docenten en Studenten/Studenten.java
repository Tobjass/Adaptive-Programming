package com.tobias.ds;

public class Studenten {

    private String naam;
    private int leeftijd;
    private String lijstMetCijfers;
    private String huisdier;

    public Studenten() {
    }

    public Studenten(String naam, int leeftijd, String lijstMetCijfers, String huisdier) {
        this.naam = naam;
        this.leeftijd = leeftijd;
        this.lijstMetCijfers = lijstMetCijfers;
        this.huisdier = huisdier;
    }
}
